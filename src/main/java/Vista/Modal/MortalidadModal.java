    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnGuardarActionPerformed
        // Validar que se haya seleccionado una granja válida
        if (cbGranja.getSelectedIndex() == 0) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Por favor seleccione una granja válida.",
                    "Validación",
                    javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Validar que se haya seleccionado un sexo válido
        if (Sexo.getSelectedIndex() == 0) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Por favor seleccione un sexo válido.",
                    "Validación",
                    javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Validar que los campos obligatorios no estén vacíos
        if (txtLote.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Por favor ingrese el lote.",
                    "Validación",
                    javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (isEdit) {
            // Aquí puedes validar los datos si es necesario
        } else {
            System.out.println("Modo creación: Guardando nuevo registro");
            // Aquí puedes validar los datos si es necesario
        }
        isSaved = true; // Indica que los datos se guardaron
        dispose(); // Cierra el modal
    }// GEN-LAST:event_btnGuardarActionPerformed

