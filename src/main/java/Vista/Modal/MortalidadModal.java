    public void setControlConsumo(ControlConsumo consumo) {
        if (consumo != null) {
            DatePick.setDate(consumo.getFecha().toLocalDate()); // Asignar la fecha
            txtLote.setText(consumo.getTipoAlimento()); // Asignar el tipo de alimento
            spCantidad.setValue(consumo.getCantidad()); // Asignar la cantidad
            txtObservaciones.setText(consumo.getObservaciones()); // Asignar las observaciones
            cbGranja.setSelectedItem(String.valueOf(consumo.getGranja())); // Asignar la granja
            spGalpon.setValue(consumo.getNumeroGalpon()); // Asignar el número de galpón
        }
    }

    public void setControlMortalidad(ControlMortalidad mortalidad) {
        if (mortalidad != null) {
            // Convertir java.sql.Date a LocalDate de forma segura
            DatePick.setDate(new java.util.Date(mortalidad.getFecha().getTime()).toInstant()
                    .atZone(java.time.ZoneId.systemDefault()).toLocalDate()); // Asignar la fecha
            txtLote.setText(mortalidad.getLote()); // Asignar el lote
            spCantidad.setValue(mortalidad.getCantidad()); // Asignar la cantidad
            txtObservaciones.setText(mortalidad.getObservaciones()); // Asignar las observaciones

            // Buscar y seleccionar la granja correspondiente en el combo (empezar desde
            // índice 1, saltando placeholder)
            Granja granjaMortalidad = mortalidad.getGranja();
            if (granjaMortalidad != null) {
                for (int i = 1; i < cbGranja.getItemCount(); i++) {
                    Object item = cbGranja.getItemAt(i);
                    if (item instanceof Granja) {
                        Granja granja = (Granja) item;
                        if (granja.getId() == granjaMortalidad.getId()) {
                            cbGranja.setSelectedIndex(i);
                            break;
                        }
                    }
                }
            }

            spGalpon.setValue(mortalidad.getNumeroGalpon()); // Asignar el número de galpón
            Sexo.setSelectedItem(mortalidad.getSexo()); // Asignar el sexo
        }
    }

