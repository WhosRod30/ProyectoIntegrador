    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnGuardarActionPerformed
        // validar campos
        if (txtLote.getText().isEmpty() || txtObservaciones.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor complete todos los campos", "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (cbGranja.getSelectedIndex() == 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor seleccione una granja válida", "Validación",
                    javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (cbSexo.getSelectedIndex() == 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor seleccione un sexo", "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (spGalpon.getValue() == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor seleccione un galpon", "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (spCantidad.getValue() == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor seleccione una cantidad", "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        // guardar ingreso
        isSaved = true;
        this.dispose();
    }// GEN-LAST:event_btnGuardarActionPerformed

