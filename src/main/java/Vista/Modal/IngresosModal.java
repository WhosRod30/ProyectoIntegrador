    public void setControlIngreso(ControlIngreso controlIngreso) {
        DatePick.setDate(controlIngreso.getFecha().toLocalDate());
        txtLote.setText(controlIngreso.getLote());
        spCantidad.setValue(controlIngreso.getCantidad());

        // Buscar y seleccionar la granja correspondiente en el combo (empezar desde
        // índice 1, saltando placeholder)
        int idGranjaIngreso = controlIngreso.getGranja();
        for (int i = 1; i < cbGranja.getItemCount(); i++) {
            Object item = cbGranja.getItemAt(i);
            if (item instanceof Granja) {
                Granja granja = (Granja) item;
                if (granja.getId() == idGranjaIngreso) {
                    cbGranja.setSelectedIndex(i);
                    break;
                }
            }
        }

        spGalpon.setValue(controlIngreso.getNumeroGalpon());
        cbSexo.setSelectedItem(controlIngreso.getSexo());
        txtObservaciones.setText(controlIngreso.getObservaciones());
    }

